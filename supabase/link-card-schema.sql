-- Creates Residue link-card tables and policies.
-- Card/profile data is tied to the signed-in auth user and email.

create extension if not exists pgcrypto;

create table if not exists public.profiles (
  id uuid primary key references auth.users(id) on delete cascade,
  auth_email text,
  name text not null default '',
  slug text not null unique,
  title text,
  bio text,
  avatar_url text,
  theme text not null default 'dark',
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  constraint profiles_slug_format check (slug ~ '^[a-z0-9]+(?:-[a-z0-9]+)*$'),
  constraint profiles_theme_check check (theme in ('dark', 'light'))
);

alter table public.profiles
  add column if not exists auth_email text;
alter table public.profiles
  add column if not exists name text not null default '';
alter table public.profiles
  add column if not exists slug text;
alter table public.profiles
  add column if not exists title text;
alter table public.profiles
  add column if not exists bio text;
alter table public.profiles
  add column if not exists avatar_url text;
alter table public.profiles
  add column if not exists theme text not null default 'dark';
alter table public.profiles
  add column if not exists created_at timestamptz not null default now();
alter table public.profiles
  add column if not exists updated_at timestamptz not null default now();

update public.profiles
set slug = coalesce(nullif(slug, ''), split_part(coalesce(auth_email, ''), '@', 1))
where slug is null or slug = '';

update public.profiles p
set auth_email = lower(u.email)
from auth.users u
where p.id = u.id
  and (p.auth_email is null or p.auth_email = '');

with normalized as (
  select
    id,
    coalesce(
      nullif(
        regexp_replace(
          regexp_replace(
            lower(coalesce(slug, split_part(coalesce(auth_email, ''), '@', 1), '')),
            '[^a-z0-9]+',
            '-',
            'g'
          ),
          '(^-+|-+$)',
          '',
          'g'
        ),
        ''
      ),
      'user-' || left(replace(id::text, '-', ''), 8)
    ) as base_slug
  from public.profiles
),
deduped as (
  select
    id,
    case
      when row_number() over (partition by base_slug order by id) = 1 then base_slug
      else base_slug || '-' || row_number() over (partition by base_slug order by id)
    end as final_slug
  from normalized
)
update public.profiles p
set slug = d.final_slug
from deduped d
where p.id = d.id
  and p.slug is distinct from d.final_slug;

alter table public.profiles
  alter column auth_email type text using lower(auth_email);

