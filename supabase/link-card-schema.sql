update public.profiles
set slug = coalesce(nullif(slug, ''), split_part(coalesce(auth_email, ''), '@', 1))
where slug is null or slug = '';

update public.profiles p
set auth_email = lower(u.email)
from auth.users u
where p.id = u.id
  and (p.auth_email is null or p.auth_email = '');

with normalized as (
  select
    id,
    coalesce(
      nullif(
        regexp_replace(
          regexp_replace(
            lower(coalesce(slug, split_part(coalesce(auth_email, ''), '@', 1), '')),
            '[^a-z0-9]+',
            '-',
            'g'
          ),
          '(^-+|-+$)',
          '',
          'g'
        ),
        ''
      ),
      'user-' || left(replace(id::text, '-', ''), 8)
    ) as base_slug
  from public.profiles
),
deduped as (
  select
    id,
    case
      when row_number() over (partition by base_slug order by id) = 1 then base_slug
      else base_slug || '-' || row_number() over (partition by base_slug order by id)
    end as final_slug
  from normalized
)
update public.profiles p
set slug = d.final_slug
from deduped d
where p.id = d.id
  and p.slug is distinct from d.final_slug;

